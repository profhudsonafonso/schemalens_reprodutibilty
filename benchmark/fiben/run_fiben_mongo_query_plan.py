def first_existing_file(data_dir: Path, possible_names: List[str]) -> Optional[Path]:
    """
    Find the first matching data file.

    This version is case-insensitive and supports recursive search.
    It fixes cases where FIBEN files are named with uppercase names,
    for example CORPORATION.csv instead of corporation.csv.
    """
    # 1. Exact direct match.
    for name in possible_names:
        path = data_dir / name
        if path.exists():
            return path

    # 2. Direct case-insensitive match.
    possible_lower = {name.lower() for name in possible_names}

    for path in data_dir.iterdir():
        if path.is_file() and path.name.lower() in possible_lower:
            return path

    # 3. Recursive case-insensitive match.
    for path in data_dir.rglob("*"):
        if path.is_file() and path.name.lower() in possible_lower:
            return path

    # 4. Stem-based match, ignoring extension and case.
    possible_stems = set()
    for name in possible_names:
        name_lower = Path(name).name.lower()

        if name_lower.endswith(".csv.gz"):
            possible_stems.add(name_lower.replace(".csv.gz", ""))
        elif name_lower.endswith(".tsv.gz"):
            possible_stems.add(name_lower.replace(".tsv.gz", ""))
        else:
            possible_stems.add(Path(name_lower).stem)

    for path in data_dir.rglob("*"):
        if not path.is_file():
            continue

        name_lower = path.name.lower()

        if name_lower.endswith(".csv.gz"):
            stem = name_lower.replace(".csv.gz", "")
        elif name_lower.endswith(".tsv.gz"):
            stem = name_lower.replace(".tsv.gz", "")
        else:
            stem = path.stem.lower()

        if stem in possible_stems:
            return path

    return None


def infer_csv_options(file_path: Path) -> Dict[str, Any]:
    suffix = file_path.suffix.lower()
    if suffix == ".tsv":
        return {"delim": "\t"}
    return {"delim": ","}


