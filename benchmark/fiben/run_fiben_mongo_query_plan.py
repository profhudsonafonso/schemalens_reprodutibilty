def direct_related_df(
    con,
    source_entity: str,
    target_entity: str,
    source_ids: List[Any],
    source_column: str,
    target_column: str,
) -> pd.DataFrame:
    if not source_ids:
        return pd.DataFrame()

    source_view = ENTITY_VIEW_MAP[source_entity]
    target_view = ENTITY_VIEW_MAP[target_entity]

    sql = f"""
    SELECT
        s.{sql_identifier(source_column)} AS source_join_key,
        t.*
    FROM {source_view} s
    JOIN {target_view} t
      ON s.{sql_identifier(source_column)} = t.{sql_identifier(target_column)}
    WHERE s.{sql_identifier(source_column)} IN {sql_in_list(source_ids)}
    """
    return con.execute(sql).df()


def reverse_direct_related_df(
    con,
    source_entity: str,
    target_entity: str,
    root_ids: List[Any],
    source_column: str,
    target_column: str,
) -> pd.DataFrame:
    if not root_ids:
        return pd.DataFrame()

    source_view = ENTITY_VIEW_MAP[source_entity]
    target_view = ENTITY_VIEW_MAP[target_entity]

    sql = f"""
    SELECT
        t.{sql_identifier(target_column)} AS root_join_key,
        s.*
    FROM {source_view} s
    JOIN {target_view} t
      ON s.{sql_identifier(source_column)} = t.{sql_identifier(target_column)}
    WHERE t.{sql_identifier(target_column)} IN {sql_in_list(root_ids)}
    """
    return con.execute(sql).df()


def norm_sql(alias: str, column: str) -> str:
    """
    Return a DuckDB SQL expression that normalizes a FIBEN join key.

    This extracts the trailing numeric component from IDs such as:
    - 1001000
    - '1001000'
    - 'SF10_SEC_R01_1001000'

    The result is a VARCHAR key that can be compared across SF1/SF10/SF30.
    """
    return (
        "regexp_extract("
        f"CAST({alias}.{sql_identifier(column)} AS VARCHAR), "
        "'([0-9]+)$'"
        ")"
    )


def norm_eq(left_alias: str, left_col: str, right_alias: str, right_col: str) -> str:
    """
    Build a normalized equality expression for DuckDB joins.
    """
    return f"{norm_sql(left_alias, left_col)} = {norm_sql(right_alias, right_col)}"



