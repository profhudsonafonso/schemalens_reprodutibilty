def parse_json_list_cell(value: Any) -> List[str]:
    if value is None:
        return []
    if isinstance(value, float) and math.isnan(value):
        return []
    if isinstance(value, list):
        return [str(v) for v in value]
    if isinstance(value, str):
        s = value.strip()
        if not s:
            return []
        try:
            obj = json.loads(s)
            if isinstance(obj, list):
                return [str(v) for v in obj]
        except Exception:
            pass
        return [v for v in s.split(";") if v]
    return [str(value)]


def aggregate_query_plan_results(component_df: pd.DataFrame) -> pd.DataFrame:
    if component_df.empty:
        return pd.DataFrame()

    group_cols = [
        "candidate_id",
        "query_name",
        "final_benchmark_group",
        "design_pattern",
        "g_class",
        "root_entity",
        "scale_label",
        "run_phase",
        "repetition",
        "execution_db_name",
    ]

    for col in group_cols:
        if col not in component_df.columns:
            component_df[col] = None

    rows = []
    for keys, grp in component_df.groupby(group_cols, dropna=False):
        kd = dict(zip(group_cols, keys))

        all_stages = set()
        all_indexes = set()
        for _, r in grp.iterrows():
            all_stages.update(parse_json_list_cell(r.get("all_stages_json")))
            all_indexes.update(parse_json_list_cell(r.get("all_index_names_json")))

        sum_n_returned = int(pd.to_numeric(grp.get("n_returned"), errors="coerce").fillna(0).sum())
        sum_docs_examined = int(pd.to_numeric(grp.get("total_docs_examined"), errors="coerce").fillna(0).sum())
        sum_keys_examined = int(pd.to_numeric(grp.get("total_keys_examined"), errors="coerce").fillna(0).sum())
        sum_execution_time_ms = float(pd.to_numeric(grp.get("execution_time_millis"), errors="coerce").fillna(0).sum())
        sum_estimated_docs_examined_bytes = float(pd.to_numeric(grp.get("estimated_docs_examined_bytes"), errors="coerce").fillna(0).sum())
        sum_estimated_returned_bytes = float(pd.to_numeric(grp.get("estimated_returned_bytes"), errors="coerce").fillna(0).sum())

        docs_per_returned_total = (
            float(sum_docs_examined) / float(sum_n_returned)
            if sum_n_returned > 0 else None
        )
        keys_per_returned_total = (
            float(sum_keys_examined) / float(sum_n_returned)
            if sum_n_returned > 0 else None
        )
        estimated_examined_bytes_per_returned = (
            sum_estimated_docs_examined_bytes / float(sum_n_returned)
            if sum_n_returned > 0 else None
        )

        status_values = set(str(v) for v in grp["execution_status"].dropna().tolist())
        if "failed" in status_values:
            execution_status = "failed"
        elif status_values == {"skipped"}:
            execution_status = "skipped"
        else:
            execution_status = "completed"

        rows.append({
            **kd,
            "execution_status": execution_status,
            "n_components": int(len(grp)),
            "n_completed_components": int((grp["execution_status"] == "completed").sum()),
            "n_failed_components": int((grp["execution_status"] == "failed").sum()),
            "sum_execution_time_millis": sum_execution_time_ms,
            "sum_n_returned": sum_n_returned,
            "sum_total_docs_examined": sum_docs_examined,
            "sum_total_keys_examined": sum_keys_examined,
            "docs_per_returned_total": docs_per_returned_total,
            "keys_per_returned_total": keys_per_returned_total,
            "has_IXSCAN": bool(grp.get("has_IXSCAN", pd.Series([False])).fillna(False).astype(bool).any()),
            "has_COLLSCAN": bool(grp.get("has_COLLSCAN", pd.Series([False])).fillna(False).astype(bool).any()),
            "has_FETCH": bool(grp.get("has_FETCH", pd.Series([False])).fillna(False).astype(bool).any()),
            "has_SORT": bool(grp.get("has_SORT", pd.Series([False])).fillna(False).astype(bool).any()),
            "has_AND_SORTED": bool(grp.get("has_AND_SORTED", pd.Series([False])).fillna(False).astype(bool).any()),
            "has_OR": bool(grp.get("has_OR", pd.Series([False])).fillna(False).astype(bool).any()),
            "has_PROJECTION": bool(grp.get("has_PROJECTION", pd.Series([False])).fillna(False).astype(bool).any()),
            "has_LOOKUP": bool(grp.get("has_LOOKUP", pd.Series([False])).fillna(False).astype(bool).any()),
            "has_GROUP": bool(grp.get("has_GROUP", pd.Series([False])).fillna(False).astype(bool).any()),
            "used_disk": bool(grp.get("used_disk", pd.Series([False])).fillna(False).astype(bool).any()),
            "all_stages": ";".join(sorted(all_stages)),
            "all_index_names": ";".join(sorted(all_indexes)),
            "query_hashes": ";".join(sorted(set(str(v) for v in grp.get("query_hash", pd.Series(dtype=object)).dropna().tolist()))),
            "plan_cache_keys": ";".join(sorted(set(str(v) for v in grp.get("plan_cache_key", pd.Series(dtype=object)).dropna().tolist()))),
            "n_rejected_plans": int(pd.to_numeric(grp.get("n_rejected_plans"), errors="coerce").fillna(0).sum()),
            "sum_estimated_docs_examined_bytes": sum_estimated_docs_examined_bytes,
            "sum_estimated_returned_bytes": sum_estimated_returned_bytes,
            "estimated_examined_bytes_per_returned": estimated_examined_bytes_per_returned,
            "max_docs_examined_collection_ratio": float(pd.to_numeric(grp.get("docs_examined_collection_ratio"), errors="coerce").max()) if pd.to_numeric(grp.get("docs_examined_collection_ratio"), errors="coerce").notna().any() else None,
            "max_index_to_data_size_ratio": float(pd.to_numeric(grp.get("index_to_data_size_ratio"), errors="coerce").max()) if pd.to_numeric(grp.get("index_to_data_size_ratio"), errors="coerce").notna().any() else None,
            "max_collection_avg_obj_size_bytes": float(pd.to_numeric(grp.get("collection_avg_obj_size_bytes"), errors="coerce").max()) if pd.to_numeric(grp.get("collection_avg_obj_size_bytes"), errors="coerce").notna().any() else None,
            "collections_touched": ";".join(sorted(set(str(v) for v in grp.get("collection_name", pd.Series(dtype=object)).dropna().tolist()))),
        })

    return (
        pd.DataFrame(rows)
        .sort_values(["scale_label", "query_name", "final_benchmark_group", "candidate_id", "run_phase", "repetition"])
        .reset_index(drop=True)
    )


