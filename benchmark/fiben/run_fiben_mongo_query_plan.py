def explain_q2_company_with_descriptors_and_listed_securities(db, cfg, param, args, run_phase: str, repetition: int) -> List[Dict[str, Any]]:
    query_name = "Q2_CompanyWithIndustryCountryAndListedSecurities"
    rows = []
    candidate_id = cfg["candidate_id"]
    embedded = set(parse_listlike_cell(cfg.get("embedded_entities")))

    component_no, root_filter, root_doc = add_candidate_root_lookup(
        rows, db, cfg, query_name, param, args, run_phase, repetition
    )

    corporation_doc = root_doc or lookup_corporation_doc(db, param)
    root_norm = normalize_join_key(param)

    if "Industry" not in embedded:
        industry_id = first_present(corporation_doc, ["ISCLASSIFIEDBY", "_norm_ISCLASSIFIEDBY"])
        if industry_id is not None:
            component_no = lookup_reference_component(
                rows, db, "Industry", [industry_id], args, query_name, candidate_id,
                run_phase, repetition, component_no, component_name="industry_lookup", role="semantic_descriptor_lookup"
            )

    if "Country" not in embedded:
        country_id = first_present(corporation_doc, ["ISDOMICILEDIN", "_norm_ISDOMICILEDIN"])
        if country_id is not None:
            component_no = lookup_reference_component(
                rows, db, "Country", [country_id], args, query_name, candidate_id,
                run_phase, repetition, component_no, component_name="country_lookup", role="semantic_descriptor_lookup"
            )

    listed_embedded = embedded_docs_from_root(root_doc, "ListedSecurity")
    if listed_embedded:
        return rows

    listed_ref_ids = reference_ids_from_root(root_doc, "ListedSecurity")
    if listed_ref_ids:
        component_no = lookup_reference_component(
            rows, db, "ListedSecurity", listed_ref_ids, args, query_name, candidate_id,
            run_phase, repetition, component_no, component_name="listed_security_lookup_by_candidate_refs", role="candidate_reference_lookup"
        )
        return rows

    # Fallback semantic path: Corporation -> Security -> ListedSecurity.
    security_filter = {"_norm_ISPROVIDEDBY": root_norm}
    component_no = append_find(
        rows, db, COLLECTION_NAME_BY_ENTITY["Security"], security_filter, args,
        query_name, candidate_id, run_phase, repetition, component_no,
        "security_lookup_by_corporation", "semantic_bridge_lookup",
        extra_metadata={"source_entity": "Corporation", "target_entity": "Security"},
    )
    securities = safe_find_many(
        db, COLLECTION_NAME_BY_ENTITY["Security"], security_filter,
        projection={"SECURITYID": 1, "_norm_SECURITYID": 1},
        limit=component_id_limit(args),
    )
    security_ids = field_values(securities, "SECURITYID") or field_values(securities, "_norm_SECURITYID")
    if security_ids:
        filt, meta = entity_lookup_filter("ListedSecurity", security_ids, args=args)
        component_no = append_find(
            rows, db, COLLECTION_NAME_BY_ENTITY["ListedSecurity"], filt, args,
            query_name, candidate_id, run_phase, repetition, component_no,
            "listed_security_lookup_by_security_ids", "semantic_target_lookup",
            extra_metadata={"source_entity": "Security", "target_entity": "ListedSecurity", **meta},
        )
    return rows


