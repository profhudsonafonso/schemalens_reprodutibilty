def initialize_execution_log_for_query_plan(log_file_path: Path, args) -> None:
    log_file_path.parent.mkdir(parents=True, exist_ok=True)
    mode = "a" if args.append_log else "w"
    with open(log_file_path, mode, encoding="utf-8") as f:
        if args.append_log:
            f.write("\n")
        f.write("# FIBEN MongoDB query-plan execution log\n")
        f.write(f"# Created at: {datetime.now().isoformat()}\n")
        f.write(f"# Command: {' '.join(sys.argv)}\n")
        f.write("#\n")
        f.write(f"# data_dir: {args.data_dir}\n")
        f.write(f"# artifacts_dir: {args.artifacts_dir}\n")
        f.write(f"# results_dir: {args.results_dir}\n")
        f.write(f"# scale_label: {args.scale_label}\n")
        f.write(f"# explain_verbosity: {args.explain_verbosity}\n")
        f.write(f"# row_limit: {args.row_limit}\n")
        f.write(f"# batch_size: {args.batch_size}\n")
        f.write("\n")


def main():
    global GLOBAL_VERBOSE, GLOBAL_BATCH_LOG_EVERY, GLOBAL_LOG_INTERVAL_SECONDS, GLOBAL_LOG_FILE_PATH

    args = build_arg_parser().parse_args()
    GLOBAL_VERBOSE = bool(args.verbose)
    GLOBAL_BATCH_LOG_EVERY = max(1, int(args.batch_log_every))
    GLOBAL_LOG_INTERVAL_SECONDS = float(args.log_interval_seconds)

    data_dir = Path(args.data_dir)
    artifacts_dir = Path(args.artifacts_dir)
    results_dir = Path(args.results_dir)
    results_dir.mkdir(parents=True, exist_ok=True)

    GLOBAL_LOG_FILE_PATH = (
        Path(args.log_file)
        if args.log_file is not None
        else results_dir / "execution.log"
    )

    initialize_execution_log_for_query_plan(Path(GLOBAL_LOG_FILE_PATH), args)
    log(f"Execution log file: {GLOBAL_LOG_FILE_PATH}")
    log(f"Command: {' '.join(sys.argv)}")

    if args.minimal_base_load:
        log("--minimal-base-load was provided, but this FIBEN query-plan runner uses safe full base loading for now.", level="WARN")

    plan_df, specs_by_id, manifest = load_benchmark_artifacts(artifacts_dir)
    selected_plan_df = select_plan_rows(plan_df, args)

    if selected_plan_df.empty:
        raise SystemExit("No benchmark configurations selected after filters.")

    selected_summary_path = results_dir / "selected_experiments_summary.csv"
    selected_plan_df.to_csv(selected_summary_path, index=False)
    log(f"Saved selected experiments summary: {selected_summary_path}")

    log(f"Selected query-plan configurations: {selected_plan_df['candidate_id'].nunique()}")
    log(
        f"Run settings: scale={args.scale_label} "
        f"row_limit={args.row_limit} batch_size={args.batch_size} "
        f"repetitions={args.repetitions} explain_one_per_query={args.explain_one_per_query} "
        f"verbosity={args.explain_verbosity} max_component_ids={args.max_component_ids}"
    )
    for _, r in selected_plan_df.drop_duplicates("candidate_id").iterrows():
        log(
            f"  - {r['candidate_id']} | query={r['query_name']} | "
            f"group={r['final_benchmark_group']} | pattern={r['design_pattern']}"
        )

    con = open_fiben_duckdb(data_dir)
    pool = build_query_parameter_pool(con, sample_size=args.sample_size)

    client = mongo_client(args)

    execution_db_name = sanitize_mongo_name(f"{args.execution_db_prefix}_{args.scale_label}")
    db = client[execution_db_name]

    initialization_rows = []
    candidate_load_rows = []
    component_frames = []

    try:
        init_summary = initialize_normalized_database(
            client=client,
            db_name=execution_db_name,
            con=con,
            row_limit=args.row_limit,
            batch_size=args.batch_size,
            force_rebuild=args.force_rebuild_db,
        )
        init_summary["scale_label"] = args.scale_label
        initialization_rows.append(init_summary)

        unique_candidates_df = selected_plan_df.drop_duplicates("candidate_id").copy()

        if args.skip_candidate_load or args.candidate_load_mode == "skip":
            log("Skipping candidate load because --skip-candidate-load or --candidate-load-mode skip was set.")
        else:
            for _, candidate_row in unique_candidates_df.iterrows():
                candidate_id = candidate_row["candidate_id"]
                spec = specs_by_id.get(candidate_id)

                if spec is None:
                    log(f"Missing candidate spec for candidate_id={candidate_id}", level="ERROR")
                    continue

                t0 = time.perf_counter()
                try:
                    if args.candidate_load_mode == "param-only":
                        candidate_plan_df = selected_plan_df[selected_plan_df["candidate_id"] == candidate_id].copy()
                        summary = materialize_candidate_configuration_param_only(
                            db=db,
                            con=con,
                            candidate_spec=spec,
                            candidate_plan_df=candidate_plan_df,
                            pool=pool,
                            args=args,
                        )
                    else:
                        summary = materialize_candidate_configuration(
                            db=db,
                            con=con,
                            candidate_spec=spec,
                            row_limit=args.row_limit,
                            batch_size=args.batch_size,
                        )
                    summary["elapsed_seconds"] = time.perf_counter() - t0
                    summary["load_status"] = "completed"
                except Exception as e:
                    summary = {
                        "candidate_id": candidate_id,
                        "collection_name": spec.get("collection_name") if spec else None,
                        "root_entity": spec.get("root_entity") if spec else None,
                        "design_pattern": spec.get("design_pattern") if spec else None,
                        "candidate_load_mode": args.candidate_load_mode,
                        "elapsed_seconds": time.perf_counter() - t0,
                        "load_status": "failed",
                        "error_message": f"{type(e).__name__}: {e}",
                    }
                    if args.verbose:
                        log(traceback.format_exc(), level="ERROR")

                candidate_load_rows.append(summary)

        phases = args.run_phase if args.run_phase else ["hot"]
        template_df = build_run_template(
            selected_plan_df,
            repetitions=args.repetitions,
            phases=phases,
            explain_one_per_query=args.explain_one_per_query,
        )

        for _, candidate_row in unique_candidates_df.iterrows():
            candidate_id = candidate_row["candidate_id"]
            spec = specs_by_id.get(candidate_id)
            if spec is None:
                continue

            cfg = dict(spec)
            cfg.update({
                "candidate_id": candidate_id,
                "query_name": candidate_row.get("query_name"),
                "collection_name": spec.get("collection_name"),
                "root_entity": spec.get("root_entity"),
                "g_class": spec.get("g_class"),
                "design_pattern": spec.get("design_pattern"),
                "final_benchmark_group": candidate_row.get("final_benchmark_group"),
            })

            component_df = run_candidate_query_plan(
                db=db,
                cfg=cfg,
                template_df=template_df,
                pool=pool,
                args=args,
            )

            if not component_df.empty:
                component_df["scale_label"] = args.scale_label
                component_df["execution_db_name"] = execution_db_name
                component_frames.append(component_df)

    finally:
        try:
            con.close()
        except Exception:
            pass
        client.close()

    if initialization_rows:
        init_path = results_dir / "scale_db_initialization_summary.csv"
        pd.DataFrame(initialization_rows).to_csv(init_path, index=False)
        log(f"Saved initialization summary: {init_path}")

    if candidate_load_rows:
        load_df = pd.DataFrame(candidate_load_rows)
        load_path = results_dir / "candidate_load_summary.csv"
        load_df.to_csv(load_path, index=False)
        log(f"Saved candidate load summary: {load_path}")

        swap_path = results_dir / "collection_swap_summary.csv"
        load_df.to_csv(swap_path, index=False)
        log(f"Saved collection/materialization summary: {swap_path}")

    summary_df = pd.DataFrame()
    component_df = pd.DataFrame()

    if component_frames:
        component_df = pd.concat(component_frames, ignore_index=True)
        component_path = results_dir / "query_plan_component_results.csv"
        component_df.to_csv(component_path, index=False)
        log(f"Saved query-plan component results: {component_path}")

        summary_df = aggregate_query_plan_results(component_df)
        summary_path = results_dir / "query_plan_summary_results.csv"
        summary_df.to_csv(summary_path, index=False)
        log(f"Saved query-plan summary results: {summary_path}")

        status_df = build_query_plan_status_summary(summary_df, component_df)
        status_path = results_dir / "query_plan_status_summary.csv"
        status_df.to_csv(status_path, index=False)
        log(f"Saved query-plan status summary: {status_path}")

        failed_df = component_df[component_df["execution_status"] == "failed"].copy()
        if not failed_df.empty:
            failed_path = results_dir / "query_plan_failed_rows.csv"
            failed_df.to_csv(failed_path, index=False)
            log(f"Saved failed query-plan rows: {failed_path}")

        if not summary_df.empty:
            zero_df = summary_df[
                (summary_df["query_name"].isin(READ_QUERY_NAMES))
                & (summary_df["execution_status"] == "completed")
                & (summary_df["sum_n_returned"].fillna(0).astype(float) == 0)
            ].copy()
            if not zero_df.empty:
                zero_path = results_dir / "query_plan_zero_returned_rows.csv"
                zero_df.to_csv(zero_path, index=False)
                log(f"Saved zero-returned query-plan rows: {zero_path}")

    if not args.no_run_manifest:
        run_manifest = {
            "script": "run_fiben_mongo_query_plan.py",
            "source_runner": "run_fiben_mongo_benchmark.py",
            "data_dir": str(data_dir),
            "artifacts_dir": str(artifacts_dir),
            "results_dir": str(results_dir),
            "scale_label": args.scale_label,
            "execution_db_name": execution_db_name,
            "execution_log_file": str(GLOBAL_LOG_FILE_PATH),
            "n_selected_candidates": int(selected_plan_df["candidate_id"].nunique()),
            "n_selected_plan_rows": int(len(selected_plan_df)),
            "query_plan_only": bool(args.query_plan_only),
            "explain_verbosity": args.explain_verbosity,
            "save_raw_explain": bool(args.save_raw_explain),
            "explain_one_per_query": bool(args.explain_one_per_query),
            "repetitions": int(args.repetitions),
            "phases": args.run_phase if args.run_phase else ["hot"],
            "row_limit": args.row_limit,
            "batch_size": args.batch_size,
            "candidate_load_mode": args.candidate_load_mode,
            "artifacts_manifest": manifest,
        }

        run_manifest_path = results_dir / "benchmark_run_manifest.json"
        with open(run_manifest_path, "w", encoding="utf-8") as f:
            json.dump(run_manifest, f, indent=2, ensure_ascii=False, default=str)

        log(f"Saved run manifest: {run_manifest_path}")

    log(f"Done. Query-plan results in: {results_dir}")


if __name__ == "__main__":
    main()
