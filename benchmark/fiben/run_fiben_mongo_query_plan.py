def component_id_limit(args) -> int:
    return max(1, int(getattr(args, "max_component_ids", 5000) or 5000))


def truncate_ids(values: Iterable[Any], args) -> Tuple[List[Any], bool, int]:
    clean = []
    seen = set()
    for value in values or []:
        if value is None:
            continue
        safe = bson_safe(value)
        key = str(safe)
        if key in seen:
            continue
        seen.add(key)
        clean.append(safe)
    original_count = len(clean)
    limit = component_id_limit(args)
    if len(clean) > limit:
        return clean[:limit], True, original_count
    return clean, False, original_count


def norm_id_list(values: Iterable[Any], args=None) -> Tuple[List[str], bool, int]:
    norm_values = []
    seen = set()
    for value in values or []:
        norm = normalize_join_key(value)
        if norm is None:
            continue
        if norm in seen:
            continue
        seen.add(norm)
        norm_values.append(norm)
    original_count = len(norm_values)
    if args is not None and len(norm_values) > component_id_limit(args):
        return norm_values[:component_id_limit(args)], True, original_count
    return norm_values, False, original_count


def in_filter(field_name: str, values: Iterable[Any], args=None) -> Tuple[Dict[str, Any], Dict[str, Any]]:
    values, truncated, original_count = truncate_ids(values, args) if args is not None else (list(values or []), False, len(list(values or [])))
    return {field_name: {"$in": values}}, {
        "n_filter_ids": len(values),
        "filter_ids_original_count": original_count,
        "filter_ids_truncated": bool(truncated),
    }


def norm_in_filter(field_name: str, values: Iterable[Any], args=None) -> Tuple[Dict[str, Any], Dict[str, Any]]:
    values, truncated, original_count = norm_id_list(values, args=args)
    return {field_name: {"$in": values}}, {
        "n_filter_ids": len(values),
        "filter_ids_original_count": original_count,
        "filter_ids_truncated": bool(truncated),
    }


def safe_find_one(db, collection_name: str, filter_doc: Dict[str, Any], projection: Optional[Dict[str, Any]] = None) -> Optional[Dict[str, Any]]:
    try:
        return db[collection_name].find_one(bson_safe_filter(filter_doc or {}), projection)
    except Exception:
        return None


def safe_find_many(
    db,
    collection_name: str,
    filter_doc: Dict[str, Any],
    projection: Optional[Dict[str, Any]] = None,
    limit: Optional[int] = None,
) -> List[Dict[str, Any]]:
    try:
        cursor = db[collection_name].find(bson_safe_filter(filter_doc or {}), projection or {})
        if limit is not None:
            cursor = cursor.limit(int(limit))
        return list(cursor)
    except Exception:
        return []


def field_values(docs: Iterable[Dict[str, Any]], field_name: str) -> List[Any]:
    values = []
    for doc in docs or []:
        if not isinstance(doc, dict):
            continue
        value = doc.get(field_name)
        if value is None:
            continue
        if isinstance(value, list):
            values.extend(value)
        else:
            values.append(value)
    return values


def first_present(doc: Optional[Dict[str, Any]], field_names: Iterable[str]) -> Any:
    if not isinstance(doc, dict):
        return None
    for field_name in field_names:
        value = doc.get(field_name)
        if value is not None:
            return value
    return None


def list_field(doc: Optional[Dict[str, Any]], field_name: str) -> List[Any]:
    if not isinstance(doc, dict):
        return []
    value = doc.get(field_name, [])
    if value is None:
        return []
    if isinstance(value, list):
        return value
    return [value]


def embedded_docs_from_root(root_doc: Optional[Dict[str, Any]], entity: str) -> List[Dict[str, Any]]:
    values = list_field(root_doc, f"{entity.lower()}_embedded")
    return [v for v in values if isinstance(v, dict)]


def reference_ids_from_root(root_doc: Optional[Dict[str, Any]], entity: str) -> List[Any]:
    values = list_field(root_doc, f"{entity.lower()}_refs")
    return [bson_safe(v) for v in values if v is not None]


def entity_lookup_filter(entity: str, ids: Iterable[Any], args=None) -> Tuple[Dict[str, Any], Dict[str, Any]]:
    pk = ENTITY_PK_MAP.get(entity)
    if not pk:
        return in_filter("_id", ids, args=args)
    return norm_in_filter(f"_norm_{pk}", ids, args=args)


