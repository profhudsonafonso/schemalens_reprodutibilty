def root_filter_for_query(query_name: str, cfg: Dict[str, Any], param: Any) -> Dict[str, Any]:
    root_entity = cfg.get("root_entity")
    root_pk = ENTITY_PK_MAP.get(root_entity)

    if query_name in {
        "Q1_CompanyProfileIBM",
        "Q2_CompanyWithIndustryCountryAndListedSecurities",
        "Q5_ReportsAndMetricDataOfCompany",
    }:
        return {"CORPORATIONID": bson_safe(param)}

    if query_name == "Q3_SecuritiesHeldInEachFinancialServiceAccount":
        return {"FINANCIALSERVICEACCOUNTID": bson_safe(param)}

    if query_name == "Q4_CompaniesReachedFromPersonThroughAccountHoldingListedSecurity":
        return {"PERSONID": bson_safe(param)}

    # Q7 and Q8 are parameterized by corporation, not by person. Do not build
    # an invalid Person root lookup with a Corporation id.
    if query_name in {
        "Q7_PersonsWhoBoughtMoreIBMThanSold",
        "Q8_IBMTransactionsBelowAverageSellingPrice",
    }:
        return {}

    if query_name == "Q9_PersonsWhoBoughtAndSoldSameStock":
        return {}

    if root_pk:
        return {root_pk: bson_safe(param)}
    return {}


def get_candidate_root_doc(db, cfg: Dict[str, Any], root_filter: Dict[str, Any]) -> Optional[Dict[str, Any]]:
    coll_name = sanitize_mongo_name(cfg.get("collection_name"))
    if not root_filter:
        return safe_find_one(db, coll_name, {})
    return safe_find_one(db, coll_name, root_filter)


def add_candidate_root_lookup(
    rows: List[Dict[str, Any]],
    db,
    cfg: Dict[str, Any],
    query_name: str,
    param: Any,
    args,
    run_phase: str,
    repetition: int,
    component_no: int = 1,
    component_name: str = "candidate_root_lookup",
    limit: Optional[int] = None,
) -> Tuple[int, Dict[str, Any], Optional[Dict[str, Any]]]:
    candidate_id = cfg["candidate_id"]
    collection_name = sanitize_mongo_name(cfg["collection_name"])
    root_filter = root_filter_for_query(query_name, cfg, param)
    effective_limit = limit if limit is not None else (1 if root_filter else None)

    next_no = append_find(
        rows=rows,
        db=db,
        collection_name=collection_name,
        filter_doc=root_filter,
        args=args,
        query_name=query_name,
        candidate_id=candidate_id,
        run_phase=run_phase,
        repetition=repetition,
        component_no=component_no,
        component_name=component_name,
        component_role="candidate_root_lookup",
        limit=effective_limit,
        extra_metadata={
            "root_entity": cfg.get("root_entity"),
            "g_class": cfg.get("g_class"),
            "design_pattern": cfg.get("design_pattern"),
        },
    )
    root_doc = get_candidate_root_doc(db, cfg, root_filter)
    return next_no, root_filter, root_doc


def lookup_reference_component(
    rows: List[Dict[str, Any]],
    db,
    entity: str,
    ids: Iterable[Any],
    args,
    query_name: str,
    candidate_id: str,
    run_phase: str,
    repetition: int,
    component_no: int,
    component_name: Optional[str] = None,
    role: str = "reference_lookup",
) -> int:
    collection_name = COLLECTION_NAME_BY_ENTITY.get(entity)
    if not collection_name:
        return component_no
    filt, meta = entity_lookup_filter(entity, ids, args=args)
    meta.update({"referenced_entity": entity})
    return append_find(
        rows=rows,
        db=db,
        collection_name=collection_name,
        filter_doc=filt,
        args=args,
        query_name=query_name,
        candidate_id=candidate_id,
        run_phase=run_phase,
        repetition=repetition,
        component_no=component_no,
        component_name=component_name or f"lookup_{entity}",
        component_role=role,
        extra_metadata=meta,
    )


def add_reference_components_from_root(
    rows: List[Dict[str, Any]],
    db,
    cfg: Dict[str, Any],
    root_doc: Optional[Dict[str, Any]],
    args,
    query_name: str,
    run_phase: str,
    repetition: int,
    component_no: int,
) -> int:
    candidate_id = cfg["candidate_id"]
    for entity in parse_listlike_cell(cfg.get("referenced_entities")):
        if entity == cfg.get("root_entity"):
            continue
        ref_ids = reference_ids_from_root(root_doc, entity)
        if not ref_ids:
            continue
        component_no = lookup_reference_component(
            rows=rows,
            db=db,
            entity=entity,
            ids=ref_ids,
            args=args,
            query_name=query_name,
            candidate_id=candidate_id,
            run_phase=run_phase,
            repetition=repetition,
            component_no=component_no,
            component_name=f"dereference_{entity}",
            role="candidate_reference_lookup",
        )
    return component_no


