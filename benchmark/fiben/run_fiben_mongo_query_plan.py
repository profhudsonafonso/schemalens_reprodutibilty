def load_json(path: Path):
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def load_benchmark_artifacts(artifacts_dir: Path):
    execution_plan_path = artifacts_dir / "benchmark_execution_plan.csv"
    specs_path = artifacts_dir / "mongodb_candidate_specs_by_candidate_id.json"
    manifest_path = artifacts_dir / "benchmark_manifest.json"

    if not execution_plan_path.exists():
        raise FileNotFoundError(f"Missing benchmark execution plan: {execution_plan_path}")

    if not specs_path.exists():
        raise FileNotFoundError(f"Missing candidate specs: {specs_path}")

    plan_df = pd.read_csv(execution_plan_path)
    specs_by_id = load_json(specs_path)
    manifest = load_json(manifest_path) if manifest_path.exists() else {}

    for col in [
        "embedded_entities",
        "referenced_entities",
        "materialized_entities",
        "active_g_classes",
        "candidate_ids",
    ]:
        if col in plan_df.columns:
            plan_df[col] = plan_df[col].apply(parse_listlike_cell)

    return plan_df, specs_by_id, manifest


def build_run_template(plan_df: pd.DataFrame, repetitions: int, phases: List[str], explain_one_per_query: bool) -> pd.DataFrame:
    rows = []
    effective_repetitions = 1 if explain_one_per_query else repetitions

    for _, plan_row in plan_df.iterrows():
        for phase in phases:
            for repetition in range(1, effective_repetitions + 1):
                row = plan_row.to_dict()
                row["run_phase"] = phase
                row["repetition"] = repetition
                rows.append(row)

    return pd.DataFrame(rows)


