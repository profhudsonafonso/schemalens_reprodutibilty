def add_physical_derived_metrics(row: Dict[str, Any]) -> Dict[str, Any]:
    avg_obj_size = row.get("collection_avg_obj_size_bytes")
    collection_size = row.get("collection_size_bytes")
    collection_count = row.get("collection_count")
    total_docs_examined = row.get("total_docs_examined") or 0
    n_returned = row.get("n_returned") or 0
    total_index_size = row.get("collection_total_index_size_bytes")

    if avg_obj_size is None and collection_size is not None and collection_count:
        try:
            avg_obj_size = float(collection_size) / float(collection_count)
            row["collection_avg_obj_size_bytes"] = avg_obj_size
        except Exception:
            pass

    estimated_docs_examined_bytes = None
    estimated_returned_bytes = None
    estimated_examined_bytes_per_returned = None
    docs_examined_collection_ratio = None
    index_to_data_size_ratio = None

    if avg_obj_size is not None:
        estimated_docs_examined_bytes = float(total_docs_examined) * float(avg_obj_size)
        estimated_returned_bytes = float(n_returned) * float(avg_obj_size)
        if n_returned > 0:
            estimated_examined_bytes_per_returned = estimated_docs_examined_bytes / float(n_returned)

    if collection_count:
        docs_examined_collection_ratio = float(total_docs_examined) / float(collection_count)

    if collection_size:
        try:
            index_to_data_size_ratio = float(total_index_size or 0) / float(collection_size)
        except Exception:
            index_to_data_size_ratio = None

    row.update({
        "estimated_docs_examined_bytes": estimated_docs_examined_bytes,
        "estimated_returned_bytes": estimated_returned_bytes,
        "estimated_examined_bytes_per_returned": estimated_examined_bytes_per_returned,
        "docs_examined_collection_ratio": docs_examined_collection_ratio,
        "index_to_data_size_ratio": index_to_data_size_ratio,
    })
    return row


def save_raw_explain_if_requested(
    explain_doc: Dict[str, Any],
    args,
    query_name: str,
    candidate_id: str,
    run_phase: str,
    repetition: int,
    component_no: int,
    collection_name: str,
) -> Optional[str]:
    if not args.save_raw_explain:
        return None

    raw_dir = Path(args.results_dir) / "query_plan_raw_json"
    raw_dir.mkdir(parents=True, exist_ok=True)

    file_name = "__".join([
        sanitize_file_stem(args.scale_label),
        sanitize_file_stem(query_name),
        sanitize_file_stem(candidate_id),
        sanitize_file_stem(run_phase),
        f"rep{int(repetition):03d}",
        f"component{int(component_no):03d}",
        sanitize_file_stem(collection_name),
    ]) + ".json"

    path = raw_dir / file_name
    with open(path, "w", encoding="utf-8") as f:
        json.dump(json_safe(explain_doc), f, indent=2, ensure_ascii=False, default=str)

    return str(path)


