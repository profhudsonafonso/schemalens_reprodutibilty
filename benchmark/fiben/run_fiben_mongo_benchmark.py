def normalize_join_key(value: Any) -> Optional[str]:
    """
    Normalize FIBEN join keys across SF1, SF10, and SF30.

    Examples
    --------
    1003507 -> "1003507"
    "1003507" -> "1003507"
    "SF10_SEC_R01_1003507" -> "1003507"

    This function extracts the trailing numeric component of an identifier.
    It allows joins between numeric foreign keys and prefixed primary keys.
    """
    if value is None:
        return None

    try:
        if pd.isna(value):
            return None
    except Exception:
        pass

    if hasattr(value, "item"):
        try:
            value = value.item()
        except Exception:
            pass

    s = str(value).strip()

    if not s:
        return None

    import re
    match = re.search(r"([0-9]+)$", s)

    if match:
        return match.group(1)

    return s



NUMERIC_VALUE_COLUMNS = {
    "HASLASTTRADEDVALUE",
    "HASPRICE",
    "HASCOUNT",
    "HASMETRICVALUE",
}


def parse_numeric_value(value: Any) -> Optional[float]:
    """
    Convert numeric-looking values to float.

    This supports values loaded as:
    - int
    - float
    - numpy scalar
    - string with dot decimal
    - string with comma decimal

    Invalid values return None.
    """
    if value is None:
        return None

    try:
        if pd.isna(value):
            return None
    except Exception:
        pass

    if hasattr(value, "item"):
        try:
            value = value.item()
        except Exception:
            pass

    if isinstance(value, (int, float)):
        return float(value)

    s = str(value).strip()

    if not s:
        return None

    # Remove thousands separators in a conservative way and support comma decimal.
    s = s.replace(" ", "")
    s = s.replace(",", ".")

    try:
        return float(s)
    except Exception:
        return None


def add_normalized_join_fields(doc: dict) -> dict:
    """
    Add normalized helper fields for FIBEN documents.

    1. _norm_<column> fields are used for join keys.
    2. _num_<column> fields are used for numeric filters and aggregations.

    This is necessary because SF10/SF30 may use prefixed IDs and may load
    some numeric fields as strings.
    """
    for col in list(doc.keys()):
        if col in JOIN_KEY_COLUMNS:
            doc[f"_norm_{col}"] = normalize_join_key(doc.get(col))

        if col in NUMERIC_VALUE_COLUMNS:
            doc[f"_num_{col}"] = parse_numeric_value(doc.get(col))

    return doc


def fetch_children_for_root_entity(
    con,
    root_entity: str,
    child_entity: str,
    root_ids: List[Any],
) -> pd.DataFrame:
    """
    Fetch one-hop related child rows for the main FIBEN paths.

    This version uses normalized join keys, because SF10/SF30 can contain
    prefixed primary keys while related foreign keys may remain numeric.

    Example:
    - SECURITY.SECURITYID = 'SF10_SEC_R01_1001000'
    - HOLDING.REFERSTO = 1001000

    Both are normalized to '1001000' before comparison.
    """
    if not root_ids:
        return pd.DataFrame()

    root_norm_ids = [
        normalize_join_key(v)
        for v in root_ids
        if normalize_join_key(v) is not None
    ]

    if not root_norm_ids:
        return pd.DataFrame()

    root_in = sql_in_list(root_norm_ids)

    # Corporation -> Industry
    if root_entity == "Corporation" and child_entity == "Industry":
        sql = f"""
        SELECT
            {norm_sql('c', 'CORPORATIONID')} AS root_id,
            i.*
        FROM fiben_corporations c
        JOIN fiben_industries i
          ON {norm_eq('c', 'ISCLASSIFIEDBY', 'i', 'INDUSTRYSECTORCLASSIFIERID')}
        WHERE {norm_sql('c', 'CORPORATIONID')} IN {root_in}
        """
        return con.execute(sql).df()

    # Corporation -> Country
    if root_entity == "Corporation" and child_entity == "Country":
        sql = f"""
        SELECT
            {norm_sql('c', 'CORPORATIONID')} AS root_id,
            co.*
        FROM fiben_corporations c
        JOIN fiben_countries co
          ON {norm_eq('c', 'ISDOMICILEDIN', 'co', 'COUNTRYID')}
        WHERE {norm_sql('c', 'CORPORATIONID')} IN {root_in}
        """
        return con.execute(sql).df()

    # Corporation -> ListedSecurity through Security.
    # Correct SF10/SF30-safe path:
    # Corporation.CORPORATIONID -> Security.ISPROVIDEDBY
    # Security.SECURITYID -> ListedSecurity.LISTEDSECURITYID
    if root_entity == "Corporation" and child_entity == "ListedSecurity":
        sql = f"""
        SELECT
            {norm_sql('c', 'CORPORATIONID')} AS root_id,
            ls.*
        FROM fiben_corporations c
        JOIN fiben_securities s
          ON {norm_eq('c', 'CORPORATIONID', 's', 'ISPROVIDEDBY')}
        JOIN fiben_listed_securities ls
          ON {norm_eq('s', 'SECURITYID', 'ls', 'LISTEDSECURITYID')}
        WHERE {norm_sql('c', 'CORPORATIONID')} IN {root_in}
        """
        return con.execute(sql).df()

    # Person -> FinancialServiceAccount
    if root_entity == "Person" and child_entity == "FinancialServiceAccount":
        sql = f"""
        SELECT
            {norm_sql('p', 'PERSONID')} AS root_id,
            a.*
        FROM fiben_persons p
        JOIN fiben_financial_service_accounts a
          ON {norm_eq('p', 'PERSONID', 'a', 'ISOWNEDBY')}
        WHERE {norm_sql('p', 'PERSONID')} IN {root_in}
        """
        return con.execute(sql).df()

    # FinancialServiceAccount -> Holding
    if root_entity == "FinancialServiceAccount" and child_entity == "Holding":
        sql = f"""
        SELECT
            {norm_sql('a', 'FINANCIALSERVICEACCOUNTID')} AS root_id,
            h.*
        FROM fiben_financial_service_accounts a
        JOIN fiben_holdings h
          ON {norm_eq('a', 'FINANCIALSERVICEACCOUNTID', 'h', 'ISHELDBY')}
        WHERE {norm_sql('a', 'FINANCIALSERVICEACCOUNTID')} IN {root_in}
        """
        return con.execute(sql).df()

    # FinancialServiceAccount -> Transaction
    if root_entity == "FinancialServiceAccount" and child_entity in ["Transaction", "BuyTransaction", "SellTransaction"]:
        sql = f"""
        SELECT
            {norm_sql('a', 'FINANCIALSERVICEACCOUNTID')} AS root_id,
            t.*
        FROM fiben_financial_service_accounts a
        JOIN fiben_transactions t
          ON {norm_eq('a', 'FINANCIALSERVICEACCOUNTID', 't', 'ISFACILITATEDBY')}
        WHERE {norm_sql('a', 'FINANCIALSERVICEACCOUNTID')} IN {root_in}
        """
        return con.execute(sql).df()

    # Holding -> ListedSecurity.
    # Physical relation goes through normalized ID compatibility.
    if root_entity == "Holding" and child_entity == "ListedSecurity":
        sql = f"""
        SELECT
            {norm_sql('h', 'HOLDINGID')} AS root_id,
            ls.*
        FROM fiben_holdings h
        JOIN fiben_listed_securities ls
          ON {norm_eq('h', 'REFERSTO', 'ls', 'LISTEDSECURITYID')}
        WHERE {norm_sql('h', 'HOLDINGID')} IN {root_in}
        """
        return con.execute(sql).df()

    # Holding -> Security.
    if root_entity == "Holding" and child_entity == "Security":
        sql = f"""
        SELECT
            {norm_sql('h', 'HOLDINGID')} AS root_id,
            s.*
        FROM fiben_holdings h
        JOIN fiben_securities s
          ON {norm_eq('h', 'REFERSTO', 's', 'SECURITYID')}
        WHERE {norm_sql('h', 'HOLDINGID')} IN {root_in}
        """
        return con.execute(sql).df()

    # Transaction -> ListedSecurity.
    if root_entity in ["Transaction", "BuyTransaction", "SellTransaction"] and child_entity == "ListedSecurity":
        sql = f"""
        SELECT
            {norm_sql('t', 'SECURITIESTRANSACTIONID')} AS root_id,
            ls.*
        FROM fiben_transactions t
        JOIN fiben_listed_securities ls
          ON {norm_eq('t', 'REFERSTO', 'ls', 'LISTEDSECURITYID')}
        WHERE {norm_sql('t', 'SECURITIESTRANSACTIONID')} IN {root_in}
        """
        return con.execute(sql).df()

    # Transaction -> Security.
    if root_entity in ["Transaction", "BuyTransaction", "SellTransaction"] and child_entity == "Security":
        sql = f"""
        SELECT
            {norm_sql('t', 'SECURITIESTRANSACTIONID')} AS root_id,
            s.*
        FROM fiben_transactions t
        JOIN fiben_securities s
          ON {norm_eq('t', 'REFERSTO', 's', 'SECURITYID')}
        WHERE {norm_sql('t', 'SECURITIESTRANSACTIONID')} IN {root_in}
        """
        return con.execute(sql).df()

    # Corporation -> FinancialReport
    if root_entity == "Corporation" and child_entity == "FinancialReport":
        sql = f"""
        SELECT
            {norm_sql('c', 'CORPORATIONID')} AS root_id,
            r.*
        FROM fiben_corporations c
        JOIN fiben_reports r
          ON {norm_eq('c', 'CORPORATIONID', 'r', 'ISPROVIDEDBY')}
        WHERE {norm_sql('c', 'CORPORATIONID')} IN {root_in}
        """
        return con.execute(sql).df()

    # FinancialReport -> ReportElement
    if root_entity == "FinancialReport" and child_entity == "ReportElement":
        sql = f"""
        SELECT
            {norm_sql('r', 'FINANCIALREPORTID')} AS root_id,
            e.*
        FROM fiben_reports r
        JOIN fiben_report_elements e
          ON {norm_eq('r', 'FINANCIALREPORTID', 'e', 'ISMEMBEROF')}
        WHERE {norm_sql('r', 'FINANCIALREPORTID')} IN {root_in}
        """
        return con.execute(sql).df()

    # ReportElement -> StatementElement
    if root_entity == "ReportElement" and child_entity == "StatementElement":
        sql = f"""
        SELECT
            {norm_sql('e', 'ELEMENTSOFFINANCIALREPORTID')} AS root_id,
            s.*
        FROM fiben_report_elements e
        JOIN fiben_statement_elements s
          ON {norm_eq('e', 'ELEMENTSOFFINANCIALREPORTID', 's', 'ELEMENTOFFINANCIALSTATEMENTID')}
        WHERE {norm_sql('e', 'ELEMENTSOFFINANCIALREPORTID')} IN {root_in}
        """
        return con.execute(sql).df()

    return pd.DataFrame()


def dataframe_rows_as_docs(df: pd.DataFrame) -> List[dict]:
    if df is None or df.empty:
        return []

    docs = []

    for row in df.to_dict(orient="records"):
        doc = json_safe(row)
        doc = add_normalized_join_fields(doc)
        docs.append(doc)

    return docs


def group_related_docs_by_root(df: pd.DataFrame, root_key: str = "root_id") -> Dict[Any, List[dict]]:
    grouped = defaultdict(list)
    if df is None or df.empty or root_key not in df.columns:
        return grouped

    for _, row in df.iterrows():
        root_id = row[root_key]
        doc = row.drop(labels=[root_key], errors="ignore").to_dict()
        doc = json_safe(doc)
        doc = add_normalized_join_fields(doc)
        grouped[root_id].append(doc)

    return grouped


