def sanitize_mongo_name(name: str) -> str:
    if name is None:
        return None
    invalid_chars = ["/", "\\", ".", "\"", "$", " ", "\x00", ":"]
    out = str(name)
    for ch in invalid_chars:
        out = out.replace(ch, "_")
    return out


def parse_listlike_cell(value: Any) -> List[Any]:
    if isinstance(value, list):
        return value
    if isinstance(value, tuple):
        return list(value)
    if value is None:
        return []
    if isinstance(value, float) and math.isnan(value):
        return []
    if isinstance(value, str):
        s = value.strip()
        if not s:
            return []
        try:
            obj = json.loads(s)
            if isinstance(obj, list):
                return obj
        except Exception:
            pass
        return [part.strip() for part in s.split(",") if part.strip()]
    return [value]


def json_safe(value: Any) -> Any:
    if value is None:
        return None
    try:
        if pd.isna(value):
            return None
    except Exception:
        pass
    if isinstance(value, dict):
        return {str(k): json_safe(v) for k, v in value.items()}
    if isinstance(value, list):
        return [json_safe(v) for v in value]
    if isinstance(value, tuple):
        return [json_safe(v) for v in value]
    if hasattr(value, "item"):
        try:
            return value.item()
        except Exception:
            pass
    return value



def bson_safe(value: Any) -> Any:
    """
    Convert values to BSON-safe Python-native types.

    PyMongo cannot encode numpy scalar values such as np.int64 directly
    inside filters or documents. This function converts them to native
    Python int, float, bool, list, or dict values.
    """
    if value is None:
        return None

    try:
        if pd.isna(value):
            return None
    except Exception:
        pass

    if isinstance(value, dict):
        return {str(k): bson_safe(v) for k, v in value.items()}

    if isinstance(value, list):
        return [bson_safe(v) for v in value]

    if isinstance(value, tuple):
        return [bson_safe(v) for v in value]

    # Converts numpy scalars, pandas scalars, etc.
    if hasattr(value, "item"):
        try:
            return value.item()
        except Exception:
            pass

    return value


def bson_safe_filter(filter_doc: Dict[str, Any]) -> Dict[str, Any]:
    """
    Convert a MongoDB filter dictionary to BSON-safe values.
    """
    return bson_safe(filter_doc)


def sql_identifier(name: str) -> str:
    return '"' + str(name).replace('"', '""') + '"'


def sql_quote(value: Any) -> str:
    return "'" + str(value).replace("'", "''") + "'"


def sql_in_list(values: Iterable[Any]) -> str:
    values = [v for v in values if v is not None]
    if not values:
        return "('')"
    return "(" + ", ".join(sql_quote(v) for v in values) + ")"


def safe_insert_many(collection, docs: List[dict]) -> int:
    if not docs:
        return 0
    docs = [json_safe(doc) for doc in docs]
    try:
        collection.insert_many(docs, ordered=False)
        return len(docs)
    except BulkWriteError as e:
        return len(docs) - len(e.details.get("writeErrors", []))


