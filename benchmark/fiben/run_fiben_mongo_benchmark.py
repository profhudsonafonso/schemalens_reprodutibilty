def log(msg: str, level: str = "INFO") -> None:
    """
    Print a log message to the terminal and also append it to the
    execution log file when GLOBAL_LOG_FILE_PATH is configured.

    Multi-line messages, such as tracebacks, are written line by line.
    """
    ts = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    message = "" if msg is None else str(msg)
    message_lines = message.splitlines() if message.splitlines() else [""]

    for message_line in message_lines:
        line = f"[{ts}] [{level}] {message_line}"
        print(line, flush=True)

        if GLOBAL_LOG_FILE_PATH is not None:
            try:
                log_path = Path(GLOBAL_LOG_FILE_PATH)
                log_path.parent.mkdir(parents=True, exist_ok=True)

                with open(log_path, "a", encoding="utf-8") as f:
                    f.write(line + "\n")
            except Exception:
                # Never allow logging failures to stop the benchmark.
                pass


