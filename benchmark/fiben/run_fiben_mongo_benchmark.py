def create_raw_view(con, view_name: str, file_path: Path) -> None:
    opts = infer_csv_options(file_path)
    delim = opts["delim"].replace("\\", "\\\\").replace("'", "''")
    file_sql = str(file_path).replace("\\", "/").replace("'", "''")

    sql = f"""
    CREATE OR REPLACE VIEW {view_name} AS
    SELECT *
    FROM read_csv_auto(
        '{file_sql}',
        delim='{delim}',
        header=true,
        ignore_errors=true,
        all_varchar=false
    );
    """
    con.execute(sql)


