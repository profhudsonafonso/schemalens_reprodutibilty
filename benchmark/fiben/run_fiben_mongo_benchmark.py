def create_entity_indexes(db, entity: str, collection_name: str) -> None:
    coll = db[collection_name]
    pk = ENTITY_PK_MAP.get(entity)

    if pk:
        try:
            coll.create_index(pk, unique=True)
        except Exception:
            coll.create_index(pk)

    common_index_candidates = [
        "HASLEGALNAME",
        "HASTICKERSYMBOL",
        "HASNAME",
        "HASPERSONNAME",
        "HASLASTTRADEDVALUE",
        "HASPRICE",
        "HASTYPE",
        "REFERSTO",
        "ISOWNEDBY",
        "ISHELDBY",
        "ISFACILITATEDBY",
        "ISPROVIDEDBY",
        "ISMEMBEROF",
    ]

    sample = coll.find_one()
    if not sample:
        return

    for col in common_index_candidates:
        if col in sample:
            try:
                coll.create_index(col)
            except Exception:
                pass

    # Index normalized join keys for SF10/SF30 compatibility.
    for col in list(sample.keys()):
        if str(col).startswith("_norm_"):
            try:
                coll.create_index(col)
            except Exception:
                pass


def load_normalized_entity_collection(
    db,
    con,
    entity: str,
    row_limit: Optional[int],
    batch_size: int,
) -> int:
    view_name = ENTITY_VIEW_MAP[entity]
    pk = ENTITY_PK_MAP[entity]
    collection_name = COLLECTION_NAME_BY_ENTITY[entity]

    coll = db[collection_name]
    coll.drop()

    total = 0
    log(f"[materialize:{collection_name}] start entity={entity}")

    sql = f"SELECT * FROM {view_name}"

    for batch_no, batch in iter_query_batches(
        con=con,
        base_sql=sql,
        order_col=pk,
        batch_size=batch_size,
        row_limit=row_limit,
    ):
        docs = dataframe_rows_as_docs(batch)
        inserted = safe_insert_many(coll, docs)
        total += inserted
        _log_collection_progress(collection_name, batch_no, len(batch), total)

    create_entity_indexes(db, entity, collection_name)
    log(f"[materialize:{collection_name}] completed total_inserted={total}")
    return total


def initialize_normalized_database(
    client: MongoClient,
    db_name: str,
    con,
    row_limit: Optional[int],
    batch_size: int,
    force_rebuild: bool,
) -> Dict[str, Any]:
    if force_rebuild:
        drop_database_if_exists(client, db_name)

    db = client[db_name]

    if not force_rebuild and db_name in client.list_database_names():
        log(f"Reusing existing normalized database: {db_name}")
        return {"db_name": db_name, "reused": True}

    log(f"=== INITIALIZING NORMALIZED FIBEN DB {db_name} ===")

    entities_to_load = [
        "Corporation",
        "Industry",
        "Country",
        "Security",
        "ListedSecurity",
        "Person",
        "FinancialServiceAccount",
        "Holding",
        "Transaction",
        "FinancialReport",
        "ReportElement",
        "StatementElement",
        "Disclosure",
    ]

    summary = {"db_name": db_name, "reused": False}

    for entity in entities_to_load:
        try:
            inserted = load_normalized_entity_collection(
                db=db,
                con=con,
                entity=entity,
                row_limit=row_limit,
                batch_size=batch_size,
            )
            summary[f"{COLLECTION_NAME_BY_ENTITY[entity]}_inserted"] = inserted
        except Exception as e:
            log(f"Failed loading entity {entity}: {type(e).__name__}: {e}", level="ERROR")
            summary[f"{COLLECTION_NAME_BY_ENTITY[entity]}_error"] = f"{type(e).__name__}: {e}"

    log(f"=== NORMALIZED FIBEN DB READY {db_name} ===")
    return summary


def build_candidate_root_docs(
    con,
    root_entity: str,
    embedded_entities: List[str],
    referenced_entities: List[str],
    row_limit: Optional[int],
    batch_size: int,
):
    """
    Yield batches of root documents for a candidate configuration.

    This implementation supports one-hop embeddings from the root.
    Deep materialized candidates can still be benchmarked as query-oriented
    collections, but this template keeps the load logic conservative.
    """
    view_name = ENTITY_VIEW_MAP[root_entity]
    pk = ENTITY_PK_MAP[root_entity]
    sql = f"SELECT * FROM {view_name}"

    for batch_no, batch in iter_query_batches(
        con=con,
        base_sql=sql,
        order_col=pk,
        batch_size=batch_size,
        row_limit=row_limit,
    ):
        root_ids = batch[pk].dropna().tolist()

        embedded_maps = {}
        for entity in embedded_entities:
            rel_df = fetch_children_for_root_entity(
                con=con,
                root_entity=root_entity,
                child_entity=entity,
                root_ids=root_ids,
            )
            embedded_maps[entity] = group_related_docs_by_root(rel_df, root_key="root_id")

        reference_maps = {}
        for entity in referenced_entities:
            rel_df = fetch_children_for_root_entity(
                con=con,
                root_entity=root_entity,
                child_entity=entity,
                root_ids=root_ids,
            )
            grouped = defaultdict(list)
            child_pk = ENTITY_PK_MAP.get(entity)
            if rel_df is not None and not rel_df.empty and child_pk in rel_df.columns:
                for _, r in rel_df.iterrows():
                    grouped[r["root_id"]].append(r[child_pk])
            reference_maps[entity] = grouped

        docs = []

        for _, row in batch.iterrows():
            root_id = row[pk]
            doc = json_safe(row.to_dict())
            doc = add_normalized_join_fields(doc)
            doc["_benchmark_root_entity"] = root_entity
            doc["_benchmark_root_id"] = root_id
            doc["_benchmark_norm_root_id"] = normalize_join_key(root_id)

            for entity in embedded_entities:
                field_name = f"{entity.lower()}_embedded"
                doc[field_name] = embedded_maps.get(entity, {}).get(root_id, [])

            for entity in referenced_entities:
                field_name = f"{entity.lower()}_refs"
                doc[field_name] = reference_maps.get(entity, {}).get(root_id, [])

            docs.append(doc)

        yield batch_no, docs


def materialize_candidate_configuration(
    db,
    con,
    candidate_spec: Dict[str, Any],
    row_limit: Optional[int],
    batch_size: int,
) -> Dict[str, Any]:
    """
    Materialize one candidate configuration into its candidate collection.
    """
    candidate_id = candidate_spec["candidate_id"]
    root_entity = candidate_spec["root_entity"]
    collection_name = sanitize_mongo_name(candidate_spec["collection_name"])

    embedded_entities = parse_listlike_cell(candidate_spec.get("embedded_entities"))
    referenced_entities = parse_listlike_cell(candidate_spec.get("referenced_entities"))
    materialized_entities = parse_listlike_cell(candidate_spec.get("materialized_entities"))

    coll = db[collection_name]
    coll.drop()

    total = 0
    log(
        f"[candidate-load] start candidate={candidate_id} "
        f"collection={collection_name} root={root_entity}"
    )

    if materialized_entities:
        # Query-oriented materialized candidate.
        # The first entity becomes the root if available.
        # This keeps the template generic and avoids overfitting to one query.
        for batch_no, docs in build_candidate_root_docs(
            con=con,
            root_entity=root_entity,
            embedded_entities=[e for e in materialized_entities if e != root_entity],
            referenced_entities=[],
            row_limit=row_limit,
            batch_size=batch_size,
        ):
            for doc in docs:
                doc["_candidate_id"] = candidate_id
                doc["_design_pattern"] = candidate_spec.get("design_pattern")
                doc["_materialized_entities"] = materialized_entities

            inserted = safe_insert_many(coll, docs)
            total += inserted
            _log_collection_progress(collection_name, batch_no, len(docs), total)
    else:
        for batch_no, docs in build_candidate_root_docs(
            con=con,
            root_entity=root_entity,
            embedded_entities=embedded_entities,
            referenced_entities=referenced_entities,
            row_limit=row_limit,
            batch_size=batch_size,
        ):
            for doc in docs:
                doc["_candidate_id"] = candidate_id
                doc["_design_pattern"] = candidate_spec.get("design_pattern")

            inserted = safe_insert_many(coll, docs)
            total += inserted
            _log_collection_progress(collection_name, batch_no, len(docs), total)

    root_pk = ENTITY_PK_MAP.get(root_entity)
    if root_pk:
        try:
            coll.create_index(root_pk)
        except Exception:
            pass

    try:
        coll.create_index("_candidate_id")
        coll.create_index("_benchmark_root_id")
    except Exception:
        pass

    log(
        f"[candidate-load] completed candidate={candidate_id} "
        f"collection={collection_name} total_inserted={total}"
    )

    return {
        "candidate_id": candidate_id,
        "collection_name": collection_name,
        "root_entity": root_entity,
        "documents_inserted": total,
    }


