def open_fiben_duckdb(data_dir: Path):
    if not data_dir.exists():
        raise FileNotFoundError(f"FIBEN data directory not found: {data_dir}")

    log(f"Opening DuckDB from FIBEN data directory: {data_dir}")
    con = duckdb.connect(database=":memory:")
    try:
        con.execute("PRAGMA disable_progress_bar")
    except Exception:
        pass

    missing = []

    for alias, possible_names in FIBEN_RAW_FILE_ALIASES.items():
        file_path = first_existing_file(data_dir, possible_names)
        if file_path is None:
            missing.append(alias)
            continue

        view_name = f"fiben_raw_{alias}"
        vlog(f"Creating DuckDB raw view {view_name} from {file_path}")
        create_raw_view(con, view_name, file_path)

    required_aliases = [
        "corporation",
        "country",
        "industrysectorclassifier",
        "listedsecurity",
        "security",
        "person",
        "financialserviceaccount",
        "holding",
        "securitiestransaction",
        "financialreport",
        "elementsoffinancialreport",
        "elementoffinancialstatement",
        "disclosure",
    ]

    required_missing = [a for a in required_aliases if a in missing]
    if required_missing:
        raise FileNotFoundError(
            "Missing required FIBEN files for aliases: "
            + ", ".join(required_missing)
        )

    create_semantic_views(con)
    return con


def iter_query_batches(
    con,
    base_sql: str,
    order_col: str,
    batch_size: int,
    row_limit: Optional[int] = None,
):
    offset = 0
    batch_no = 0

    while True:
        current_limit = batch_size
        if row_limit is not None:
            remaining = row_limit - offset
            if remaining <= 0:
                break
            current_limit = min(current_limit, remaining)

        sql = f"""
        {base_sql}
        ORDER BY {sql_identifier(order_col)}
        LIMIT {current_limit}
        OFFSET {offset}
        """
        df = con.execute(sql).df()
        if df.empty:
            break

        batch_no += 1
        yield batch_no, df
        offset += len(df)


